<template>
  <!-- docs view -->
  <div class="flex flex-column overflow-y-auto" v-if="route_view == 'docs'">
    <div
      class="flex flex-column justify-center bg-nearer-white h-100"
      v-if="is_local_fetching"
    >
      <Spinner size="large" message="Loading functions..." />
    </div>

    <PipeDocumentAddonEditor
      class="pa4"
      :pipe-eid="pipe.eid"
      :is-editing="false"
      v-else-if="has_pipe"
    />

    <!-- pipe not found -->
    <PageNotFound class="flex-fill" v-else />
  </div>

  <!-- if the user's free trial has expired, show them this message -->
  <div class="flex flex-column bg-nearer-white " v-else-if="hasFreeTrialExpired()">
    <div class="flex-fill ph4 pv5 overflow-y-scroll">
      <div class="w-100 center mw-doc ">
        <h1 class="mt0 mb4 tc f3">Your free trial period has ended. Please choose your plan.</h1>
        <BillingEditPanel class="pa4 bg-white br2 css-white-box" />
      </div>
    </div>
  </div>

  <!-- fetching -->
  <div v-else-if="is_fetching">
    <div class="flex flex-column justify-center bg-nearer-white h-100">
      <Spinner size="large" message="Loading functions..." />
    </div>
  </div>

  <!-- fetched -->
  <div class="flex flex-column bg-nearer-white" v-else-if="is_fetched">
    <div class="flex-fill flex flex-row relative" v-if="pipes.length > 0">
      <template v-if="has_pipe">
        <!-- sidebar -->
        <div class="flex flex-column min-w5 bg-white br b--black-05">
          <!-- control bar -->
          <div class="flex-none pa2 relative bg-white">
            <div class="flex flex-row items-center">
              <el-input
                class="w-100 mr2"
                size="small"
                placeholder="Search..."
                clearable
                prefix-icon="el-icon-search"
                @keydown.esc.native="pipe_list_filter = ''"
                v-model="pipe_list_filter"
              />
              <NewFunctionDropdown
                width="290"
                @click="onNewItem"
                v-require-rights:pipe.create
              >
                <el-button
                  slot="reference"
                  size="small"
                  type="primary"
                  class="ttu fw6"
                  v-require-rights:pipe.create
                >
                  New<i class="el-icon-arrow-down el-icon--right fw6" style="margin-right: -2px"></i>
                </el-button>
              </NewFunctionDropdown>
            </div>
          </div>

          <!-- list -->
          <div
            style="max-width: 20rem"
            v-show="pipe_list_filter.length > 0 && filtered_pipes.length == 0"
          >
            <div
              class="lh-title silver i"
              style="padding: 2px 0 2px 8px; margin: 0 12px 0 0; font-size: 13px"
            >
              There are no functions that match the search criteria
            </div>
          </div>
          <div
            style="max-width: 20rem"
            v-show="filtered_pipes.length > 0"
            v-bar
          >
            <el-collapse
              class="el-collapse--plain el-collapse--arrow-left"
              v-model="expanded_groups"
            >
              <el-collapse-item
                style="margin-left: 8px"
                :key="group.id"
                :name="group.id"
                v-show="
                  group.is_updating ||
                  group.pipes.length > 0 ||
                  group.pipes.length == 0 && pipe_list_filter.length == 0 // no items to show
                "
                v-for="group in grouped_pipes"
              >
                <div
                  class="flex flex-row items-center group-item-title"
                  slot="title"
                >
                  <ServiceIcon
                    class="group-item-icon"
                    :url="group.icon"
                    :type="group.connection_type"
                    v-if="group.connection_type.length > 0"
                  />
                  <i
                    class="material-icons"
                    style="margin-right: 8px"
                    v-else
                  >
                    home
                  </i>
                  <div class="truncate flex-fill fw6 mr2">{{group.title}}</div>
                  <el-dropdown
                    trigger="click"
                    v-show="group.id != 'local'"
                  >
                    <div
                      class="el-dropdown-link pointer mr2"
                      @click.stop
                    >
                      <svg class="octicon octicon-gear black-30 hover-black-60" viewBox="0 0 14 16" version="1.1" width="14" height="16" aria-hidden="true"><path fill-rule="evenodd" d="M14 8.77v-1.6l-1.94-.64-.45-1.09.88-1.84-1.13-1.13-1.81.91-1.09-.45-.69-1.92h-1.6l-.63 1.94-1.11.45-1.84-.88-1.13 1.13.91 1.81-.45 1.09L0 7.23v1.59l1.94.64.45 1.09-.88 1.84 1.13 1.13 1.81-.91 1.09.45.69 1.92h1.59l.63-1.94 1.11-.45 1.84.88 1.13-1.13-.92-1.81.47-1.09L14 8.75v.02zM7 11c-1.66 0-3-1.34-3-3s1.34-3 3-3 3 1.34 3 3-1.34 3-3 3z"></path></svg>
                    </div>
                    <el-dropdown-menu style="min-width: 10rem" slot="dropdown">
                      <el-dropdown-item
                        class="flex flex-row items-center"
                        @click.native="onEditFunctionMount(group.connection)"
                      >
                        <i class="material-icons mr2">edit</i> Edit
                      </el-dropdown-item>
                      <el-dropdown-item
                        class="flex flex-row items-center"
                        @click.native="onSyncFunctionMount(group.connection)"
                      >
                        <i class="material-icons mr2">refresh</i> Refresh
                      </el-dropdown-item>
                      <el-dropdown-item divided></el-dropdown-item>
                      <el-dropdown-item
                        class="flex flex-row items-center"
                        @click.native="onDeleteFunctionMount(group.connection)"
                      >
                        <i class="material-icons mr2">delete</i> Remove
                      </el-dropdown-item>
                    </el-dropdown-menu>
                  </el-dropdown>
                </div>

                <div
                  style="padding: 2px 0 2px 0; margin: 0 12px 4px 0; font-size: 13px"
                  v-if="group.is_updating"
                >
                  <div class="pa3 br2 import-notice">
                    <div class="flex flex-row items-center">
                      <Spinner size="small" />
                      <span class="fw6 ml2">Importing...</span>
                    </div>
                    <div class="pt2 pr2 lh-title silver i">NOTE: This process may take a few minutes to complete</div>
                  </div>
                </div>
                <div
                  class="lh-title silver i"
                  style="padding: 2px 0 2px 12px; margin: 0 12px 0 0; font-size: 13px"
                  v-else-if="group.pipes.length == 0"
                >
                  <span v-if="pipe_list_filter.length > 0">There are no functions that match the search criteria</span>
                  <span v-else>There are no functions to show</span>
                </div>
                <PipeList
                  item-style="padding: 2px 0 2px 12px; margin: 0 12px 0 0"
                  item-size="mini"
                  :items="group.pipes"
                  :selected-item.sync="pipe"
                  :show-delete="true"
                  @item-activate="selectPipe"
                  @item-delete="tryDeletePipe"
                />
              </el-collapse-item>
            </el-collapse>
          </div>
        </div>

        <!-- content area -->
        <div
          class="flex-fill ph4 pv5 overflow-y-scroll relative"
          :id="scrollbar_container_id"
        >
          <PipeDocument
            class="w-100 center mw-doc pa4 bg-white br2 css-white-box"
            style="min-height: 20rem; margin-bottom: 15rem"
            :pipe-eid="pipe.eid"
            :scrollbar-container-id="scrollbar_container_id"
            :show-test-button="!show_test_panel"
            @test-click="show_test_panel = true"
            @edit-click="onEditPipe"
          />
        </div>

        <PipeDocumentTestPanel
          class="min-w5 bg-white bl b--black-05"
          style="width: 25rem"
          :pipe-eid="pipe.eid"
          :visible.sync="show_test_panel"
        />
      </template>

      <!-- pipe not found -->
      <PageNotFound class="flex-fill bg-nearer-white" v-else />
    </div>
    <EmptyItem class="flex flex-column items-center justify-center h-100" v-else>
      <div class="tc f3" slot="text">
        <p>No functions to show</p>
        <NewFunctionDropdown
          width="290"
          @click="onNewItem"
          v-require-rights:pipe.create
        >
          <el-button
            slot="reference"
            size="large"
            type="primary"
            class="ttu fw6"
            v-require-rights:pipe.create
          >
            New<i class="el-icon-arrow-down el-icon--right fw6" style="margin-right: -2px"></i>
          </el-button>
        </NewFunctionDropdown>
      </div>
    </EmptyItem>

    <!-- pipe edit dialog -->
    <el-dialog
      custom-class="el-dialog--no-header el-dialog--no-footer"
      width="36rem"
      top="4vh"
      :modal-append-to-body="false"
      :close-on-click-modal="false"
      :visible.sync="show_pipe_dialog"
    >
      <PipeEditPanel
        :mode="pipe_edit_mode"
        :pipe="pipe_edit_mode == 'edit' ? edit_pipe : new_pipe_attrs"
        @close="show_pipe_dialog = false"
        @cancel="show_pipe_dialog = false"
        @update-pipe="onPipeUpdated"
        v-if="show_pipe_dialog"
      />
    </el-dialog>

    <!-- function mount edit dialog -->
    <el-dialog
      custom-class="el-dialog--no-header el-dialog--no-footer"
      width="48rem"
      top="4vh"
      :modal-append-to-body="false"
      :close-on-click-modal="false"
      :visible.sync="show_mount_dialog"
    >
      <FunctionMountEditPanel
        :mode="mount_edit_mode"
        :mount-type="mount_type"
        :mount="mount_edit_mode == 'edit' ? edit_connection : new_connection_attrs"
        :active-step="mount_edit_mode == 'edit' ? 'setup-config' : undefined"
        @close="show_mount_dialog = false"
        @cancel="show_mount_dialog = false"
        @submit="onFunctionMountSubmitted"
        v-if="show_mount_dialog"
      />
    </el-dialog>
  </div>
</template>

<script>
  import randomstring from 'randomstring'
  import axios from 'axios'
  import { mapState, mapGetters } from 'vuex'
  import { OBJECT_STATUS_AVAILABLE, OBJECT_STATUS_UPDATING } from '@/constants/object-status'
  import { btoaUnicode } from '@/utils'
  import Spinner from 'vue-simple-spinner'
  import NewFunctionDropdown from '@/components/NewFunctionDropdown'
  import PipeList from '@/components/PipeList'
  import PipeDocument from '@/components/PipeDocument'
  import PipeDocumentAddonEditor from '@/components/PipeDocumentAddonEditor'
  import PipeDocumentTestPanel from '@/components/PipeDocumentTestPanel'
  import PipeEditPanel from '@/components/PipeEditPanel'
  import FunctionMountEditPanel from '@/components/FunctionMountEditPanel'
  import ServiceIcon from '@/components/ServiceIcon'
  import EmptyItem from '@/components/EmptyItem'
  import BillingEditPanel from '@/components/BillingEditPanel'
  import PageNotFound from '@/components/PageNotFound'
  import MixinConnection from '@/components/mixins/connection'
  import MixinFilter from '@/components/mixins/filter'

  const CONNECTION_MODE_RESOURCE = 'R'
  const CONNECTION_MODE_FUNCTION = 'F'

  const code_python = btoaUnicode(`# basic hello world example
def flex_handler(flex):
    flex.end([["H","e","l","l","o"],["W","o","r","l","d"]])
`)

  const getNameSuffix = (length) => {
    return randomstring.generate({
      length,
      charset: 'alphabetic',
      capitalization: 'lowercase'
    })
  }

  const getDefaultAttrs = () => {
    return {
      deploy_mode: 'R',
      deploy_api: 'A',
      task: {
        op: 'sequence',
        items: []
      }
    }
  }

  const getDefaultState = () => {
    return {
      scrollbar_container_id: _.uniqueId('content-'),
      is_local_fetching: false,
      is_selecting: false,
      show_test_panel: false,
      pipe_list_filter: '',
      pipe: {},
      last_selected: {},
      expanded_groups: [],

      show_pipe_dialog: false,
      pipe_edit_mode: 'add',
      edit_pipe: null,
      new_pipe_attrs: {},

      show_mount_dialog: false,
      mount_edit_mode: 'add',
      mount_type: 'integration',
      edit_connection: null,
      new_connection_attrs: {
        connection_mode: CONNECTION_MODE_FUNCTION
      },
      polling_function_mounts: [],
    }
  }

  export default {
    metaInfo() {
      return {
        title: _.get(this.pipe, 'name', 'Functions'),
        titleTemplate: (chunk) => {
          return chunk ? `${chunk} | ${this.getActiveTeamLabel()} | Flex.io` : 'Functions | Flex.io'
        }
      }
    },
    mixins: [MixinConnection, MixinFilter],
    components: {
      Spinner,
      NewFunctionDropdown,
      PipeList,
      PipeDocument,
      PipeDocumentAddonEditor,
      PipeDocumentTestPanel,
      PipeEditPanel,
      FunctionMountEditPanel,
      ServiceIcon,
      EmptyItem,
      BillingEditPanel,
      PageNotFound
    },
    watch: {
      route_object_name: {
        handler: 'loadPipe',
        immediate: true
      },
      updating_function_mounts: {
        handler: 'pollUpdatingFunctionMounts',
        immediate: true
      },
      pipes(val, old_val) {
        if (!this.has_pipe) {
          this.loadPipe(this.route_object_name)
        }
      },
    },
    data() {
      return getDefaultState()
    },
    computed: {
      // mix this into the outer object with the object spread operator
      ...mapState({
        is_fetching: state => state.pipes.is_fetching,
        is_fetched: state => state.pipes.is_fetched,
        active_team_name: state => state.teams.active_team_name
      }),
      function_mounts() {
        return this.getAvailableFunctionMounts()
      },
      updating_function_mounts() {
        return _.filter(this.function_mounts, { eid_status: OBJECT_STATUS_UPDATING })
      },
      pipes() {
        return this.getAllPipes()
      },
      sorted_pipes() {
        return _.sortBy(this.pipes, ['name'])
      },
      filtered_pipes() {
        return this.$_Filter_filter(this.sorted_pipes, this.pipe_list_filter, ['name'])
      },
      grouped_pipes() {
        // start with an object with connection eids for keys and empty arrays for values
        var all_mounts = _.keyBy(this.function_mounts, 'eid')
        all_mounts = _.mapValues(all_mounts, m => [])
        all_mounts[''] = [] // include 'Local'

        // group all pipes by their parent connection (this will also result in
        // an object with connection eids as the key values)
        var mounts_with_pipes = _.groupBy(this.filtered_pipes, p => _.get(p, 'parent.eid', 'local'))

        // overwrite keys in 'all_mounts' with keys in 'mounts_with_pipes'
        var groups = _.assign({}, all_mounts, mounts_with_pipes)

        // return the following object for each function mount
        groups = _.map(groups, (val, key) => {
          var connection = _.find(this.function_mounts, { eid: key })

          return {
            id: key.length == 0 ? 'local' : key,
            icon: _.get(connection, 'icon', ''),
            is_updating: _.get(connection, 'eid_status', '') == OBJECT_STATUS_UPDATING,
            connection_type: _.get(connection, 'connection_type', ''),
            title: key.length == 0 ? 'My Functions' : _.get(connection, 'name', `Not found (${key})`),
            pipes: _.sortBy(val, ['name']),
            connection,
          }
        })

        // sort the groups (local functions first, then alphabetically)
        return _.sortBy(groups, [
          group => group.id != 'local',
          group => group.title.indexOf('Not found') != -1,
          group => group.title.toLowerCase()
        ])
      },
      route_object_name() {
        return _.get(this.$route, 'params.object_name', undefined)
      },
      route_view() {
        return _.get(this.$route, 'params.view', undefined)
      },
      pname() {
        return _.get(this.pipe, 'name', '')
      },
      has_pipe() {
        return this.pname.length > 0
      },
    },
    created() {
      if (this.route_view == 'docs') {
        this.tryFetchPipe()
      } else {
        this.tryFetchPipes()
      }
    },
    mounted() {
      this.initSelf()
    },
    methods: {
      ...mapGetters('teams', {
        'getActiveTeamLabel': 'getActiveTeamLabel'
      }),
      ...mapGetters('pipes', {
        'getAllPipes': 'getAllPipes'
      }),
      ...mapGetters('connections', {
        'getAvailableFunctionMounts': 'getAvailableFunctionMounts'
      }),
      ...mapGetters('users', {
        'hasFreeTrialExpired': 'hasFreeTrialExpired'
      }),
      initSelf() {
        // make sure all nodes on the collapser are expanded by default
        this.expanded_groups = _.map(this.grouped_pipes, g => g.id)
      },
      tryFetchPipe() {
        var team_name = this.active_team_name
        var name = this.route_object_name

        this.is_local_fetching = true
        this.$store.dispatch('pipes/fetch', { team_name, name }).then(response => {
          this.pipe = response.data
        }).finally(() => {
          this.is_local_fetching = false
        })
      },
      tryFetchPipes() {
        var team_name = this.active_team_name

        if (!this.is_fetched && !this.is_fetching) {
          this.$store.dispatch('pipes/fetch', { team_name })
        }
      },
      tryDeletePipe(attrs) {
        var eid = _.get(attrs, 'eid', '')
        var pname = _.get(attrs, 'name', 'Function')
        var team_name = this.active_team_name

        var msg = `Are you sure you want to delete the function named <strong>"${pname}"</strong>?`
        var title = `Really delete function?`

        this.$confirm(msg, title, {
          type: 'warning',
          confirmButtonClass: 'ttu fw6',
          cancelButtonClass: 'ttu fw6',
          confirmButtonText: 'Delete function',
          cancelButtonText: 'Cancel',
          dangerouslyUseHTMLString: true,
        }).then(() => {
          var selected_idx = _.findIndex(this.sorted_pipes, { eid: this.pipe.eid })
          var deleting_idx = _.findIndex(this.sorted_pipes, { eid: attrs.eid })

          this.$store.dispatch('pipes/delete', { team_name, eid }).then(response => {
            if (deleting_idx >= 0 && deleting_idx == selected_idx) {
              if (deleting_idx >= this.sorted_pipes.length) {
                deleting_idx--
              }

              var pipe = _.get(this.sorted_pipes, '['+deleting_idx+']', {})
              this.selectPipe(pipe)
            }
          })
        }).catch(error => {
          // do nothing
        })
      },
      tryDeleteFunctionMount(attrs) {
        var eid = _.get(attrs, 'eid', '')
        var cname = _.get(attrs, 'name', 'Connection')
        var team_name = this.active_team_name

        var msg = `<p>Removing a function mount will also remove all functions associated with the function mount.</p><div class="h1"></div><p>Are you sure you want to remove the function mount named <strong>"${cname}"</strong>?</p>`
        var title = `Really remove function mount?`

        this.$confirm(msg, title, {
          type: 'warning',
          customClass: 'el-message-box--center-reset el-message-box--wide',
          confirmButtonClass: 'ttu fw6',
          cancelButtonClass: 'ttu fw6',
          confirmButtonText: 'Remove function mount',
          cancelButtonText: 'Cancel',
          dangerouslyUseHTMLString: true,
          center: true,
        }).then(() => {
          this.$store.dispatch('connections/delete', { team_name, eid }).then(response => {
            // remove the item from the list of open collapser items
            _.remove(this.expanded_groups, item => item == eid)
          })
        })
      },
      syncFunctionMount(connection) {
        var team_name = this.active_team_name
        var eid = _.get(connection, 'eid', '')

        this.$store.dispatch('connections/sync', { team_name, eid })
        this.show_mount_dialog = false

        // add the item to the list of open collapser items
        if (this.expanded_groups.indexOf(eid) == -1) {
          this.expanded_groups = this.expanded_groups.concat([eid])
        }
      },
      pollUpdatingFunctionMounts() {
        this.updating_function_mounts.forEach(mount => {
          var mount_eid = _.get(mount, 'eid', '')
          if (this.polling_function_mounts.indexOf(mount_eid) == -1) {
            this.pollUpdatingFunctionMount(mount)
          }
        })
      },
      pollUpdatingFunctionMount(mount) {
        var team_name = this.active_team_name
        var eid = _.get(mount, 'eid', '')
        var params = {
          parent_eid: eid,
          eid_status: 'A,P'
        }

        if (eid.length > 0) {
          // make sure we know we're polling this connection
          if (this.polling_function_mounts.indexOf(eid) == -1) {
            this.polling_function_mounts.push(eid)
          }

          // query these objects fresh
          axios.all([
            this.$store.dispatch('connections/fetch', { team_name, eid }),
            this.$store.dispatch('pipes/fetchFromMount', { team_name, params }),
          ])
          .then(axios.spread((connections_response, pipes_response) => {
            var mount = connections_response.data
            var status = _.get(mount, 'eid_status', '')

            if (status == OBJECT_STATUS_UPDATING) {
              setTimeout(() => { this.pollUpdatingFunctionMount(mount) }, 1000)
            } else if (status == OBJECT_STATUS_AVAILABLE) {
              // make sure we know we're no longer polling this connection
              this.polling_function_mounts = _.filter(this.polling_function_mounts, val => val != eid)
            }
          }))
          .catch(() => {

          })
        }
      },
      loadPipe(identifier) {
        var pipe

        if (identifier) {
          pipe = _.find(this.sorted_pipes, p => p.eid == identifier || p.name == identifier)
        } else {
          // select the first pipe in the first group
          var group = _.first(this.grouped_pipes)
          pipe = _.first(group.pipes)

          // fallback scenario
          if (_.isNil(pipe)) {
            pipe = _.first(this.sorted_pipes)
          }
        }

        this.selectPipe(pipe)
      },
      selectPipe(item) {
        // we need to load the pipes first
        if (this.sorted_pipes.length == 0) {
          return
        }

        // we couldn't find the item; show a 404
        if (_.isNil(item)) {
          this.pipe = {}
          this.last_selected = {}
          return
        }

        this.pipe = _.cloneDeep(item)
        this.last_selected = _.cloneDeep(item)

        if (!this.is_selecting) {
          // update the route
          var name = _.get(item, 'name', '')
          var object_name = name.length > 0 ? name : _.get(item, 'eid', '')
          this.updateRoute(object_name)

          this.is_selecting = true
          this.$nextTick(() => { this.is_selecting = false })
        }
      },
      updateRoute(object_name) {
        var team_name = this.active_team_name
        var new_route = _.pick(this.$route, ['name', 'meta', 'params', 'path'])
        new_route.params = _.assign({}, new_route.params, { team_name, object_name })
        this.$router[!this.route_object_name?'replace':'push'](new_route)
      },
      filterByFunctionMount(connection) {
        return this.$_Connection_isFunctionMount(connection)
      },
      getNewPipeAttributes(op) {
        var step = { op }

        switch (op) {
          case 'execute':
            _.assign(step, { lang: 'python', code: code_python })
            break
          case 'extract':
            _.assign(step, { path: '' })
            break
          case 'lookup':
            _.assign(step, { path: '', lookup_keys: [], return_columns: [] })
            break
        }

        var name = op + '-' + getNameSuffix(4)
        var task = {
          op: 'sequence',
          items: [step]
        }

        return _.assign({}, getDefaultAttrs(), { name, task })
      },
      onPipeUpdated(pipe) {
        this.selectPipe(pipe)
        this.show_pipe_dialog = false
      },
      onNewItem(type) {
        switch (type) {
          case 'execute':
            this.$store.track('Clicked New Execute')
            this.pipe_edit_mode = 'add'
            this.new_pipe_attrs = this.getNewPipeAttributes('execute')
            this.show_pipe_dialog = true
            return

          case 'extract':
            this.$store.track('Clicked New Extract')
            this.pipe_edit_mode = 'add'
            this.new_pipe_attrs = this.getNewPipeAttributes('extract')
            this.show_pipe_dialog = true
            return

          case 'lookup':
            this.$store.track('Clicked New Lookup')
            this.pipe_edit_mode = 'add'
            this.new_pipe_attrs = this.getNewPipeAttributes('lookup')
            this.show_pipe_dialog = true
            return

          case 'integration':
            this.$store.track('Clicked New Integration')
            this.mount_edit_mode = 'add'
            this.mount_type = 'integration'
            this.show_mount_dialog = true
            return

          case 'mount':
            this.$store.track('Clicked New Function Mount')
            this.mount_edit_mode = 'add'
            this.mount_type = 'mount'
            this.show_mount_dialog = true
            return
        }
      },
      onEditPipe(pipe) {
        this.pipe_edit_mode = 'edit'
        this.edit_pipe = pipe
        this.show_pipe_dialog = true
      },
      onEditFunctionMount(connection) {
        this.mount_edit_mode = 'edit'
        this.edit_connection = _.cloneDeep(connection)
        this.show_mount_dialog = true
      },
      onFunctionMountSubmitted(connection) {
        if (this.mount_edit_mode == 'add') {
          this.syncFunctionMount(connection)
        } else {
          // we're done; just close the dialog
          this.show_mount_dialog = false
        }
      },
      onSyncFunctionMount(connection) {
        this.syncFunctionMount(connection)
      },
      onDeleteFunctionMount(connection) {
        this.tryDeleteFunctionMount(connection)
      }
    }
  }
</script>

