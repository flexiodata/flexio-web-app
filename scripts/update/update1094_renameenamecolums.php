include_once __DIR__.'/../stub.php';


if ($argc != 5)
{
    echo '{ "success": false, "msg": "Usage: php update*.php <host> <username> <password> <database_name>" }';
    exit(0);
}


$params = array('host' => $argv[1],
                'port' => 5432,
                'username' => $argv[2],
                'password' => $argv[3],
                'dbname' => $argv[4]);

try
{
    $db = \Flexio\Base\Db::factory('PDO_POSTGRES', $params);
    $conn = $db->getConnection();
}
catch (\Exception $e)
{
    echo($e->getMessage());
    $db = null;
}

if (is_null($db))
{
    echo '{ "success": false, "msg": "Could not connect to database." }';
    exit(0);
}


try
{
    // STEP 1: rename the ename columns to alias
    $db->exec('alter table tbl_pipe rename column ename to alias;');
    $db->exec('alter table tbl_connection rename column ename to alias;');

    // STEP 2: drop existing indexes with ename in name
    $db->exec('drop index if exists idx_pipe_ename;');
    $db->exec('drop index if exists idx_connection_ename;');

    // STEP 3: create new indexes on the alias column
    $db->exec('create index idx_pipe_alias ON tbl_pipe (alias);');
    $db->exec('create index idx_connection_alias ON tbl_connection (alias);');
}
catch(\Exception $e)
{
    echo '{ "success": false, "msg":' . json_encode($e->getMessage()) . '}';
    exit(0);
}


// update the version number
$current_version = \Flexio\System\System::getUpdateVersionFromFilename(__FILE__);
\Flexio\System\System::getModel()->setDbVersionNumber($current_version);

echo '{ "success": true, "msg": "Operation completed successfully." }';

