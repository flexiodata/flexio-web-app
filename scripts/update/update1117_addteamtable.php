include_once __DIR__.'/../stub.php';


if ($argc != 5)
{
    echo '{ "success": false, "msg": "Usage: php update*.php <host> <username> <password> <database_name>" }';
    exit(0);
}


$params = array('host' => $argv[1],
                'port' => 5432,
                'username' => $argv[2],
                'password' => $argv[3],
                'dbname' => $argv[4]);

try
{
    $db = \Flexio\Base\Db::factory('PDO_POSTGRES', $params);
    $conn = $db->getConnection();
}
catch (\Exception $e)
{
    echo($e->getMessage());
    $db = null;
}

if (is_null($db))
{
    echo '{ "success": false, "msg": "Could not connect to database." }';
    exit(0);
}


try
{
    // STEP 1: make sure the stream table doesn't exist
    $sql = <<<EOT
        DROP TABLE IF EXISTS tbl_teammember;
EOT;
    $db->exec($sql);


    // STEP 2: add the stream table
    $sql = <<<EOT
    CREATE TABLE tbl_teammember (
        id serial,
        team_eid varchar(12) NOT NULL default '',
        member_eid varchar(12) NOT NULL default '',
        member_status varchar(1) NOT NULL default 'I',
        rights text default '',
        created timestamp NULL default NULL,
        updated timestamp NULL default NULL,
        PRIMARY KEY (id),
        UNIQUE (team_eid,member_eid),
        UNIQUE (member_eid,team_eid)
      );
EOT;
    $db->exec($sql);
}
catch(\Exception $e)
{
    echo '{ "success": false, "msg":' . json_encode($e->getMessage()) . '}';
    exit(0);
}


// update the version number
$current_version = \Flexio\System\System::getUpdateVersionFromFilename(__FILE__);
\Flexio\System\System::getModel()->setDbVersionNumber($current_version);

echo '{ "success": true, "msg": "Operation completed successfully." }';
